let i = 0;
function play(id){
  let audioPlayer = document.getElementById('audio');
  audioPlayer.src=mp3[id];
  [...document.querySelectorAll(`.audio`)].forEach(v=>v.setAttribute("playing",false));
  document.querySelector(`.audio-${id}`).setAttribute("playing",true);
  audioPlayer.load();
  audioPlayer.play();
}
function setup(){
  let audioPlayer = document.getElementById('audio');
  let playlist = document.getElementById('playlist');
  mp3.forEach((v,i)=>{
    playlist.innerHTML+=`
      <li class="audio audio-${i}" onclick="play(${i})">${v.split("/")[5]}</li>
    `;
  });
  play(i);
  audioPlayer.pause();
  audioPlayer.addEventListener('ended',()=>{
    i++;
    play(i);
    if(i == mp3.length-1) i = 0;
  }, false);
}
setup();